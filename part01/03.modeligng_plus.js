// 9. 관계 (Relationship)
// - 두 개의 엔티티 타입 사이의 논리적 관계
// - 엔터티와 엔터티가 존재의 형태/행위로서 서로에게 영향
// - 데이터 모델에서 관계 = 업무의 흐름
// - 데이터 모델 관계 제대로 x => 데이터 모델 표현되지 않아 데이터의 무결성 위반 발생함

// 9-1) 관계의 표기법 
// - 관계명(Membership) : 관계 이름
// - 관계차수(Cardinality) : 1:1, 1:N, M:N
// - 관계선택사양(Optionality) : 필수 관계, 선택관계

// 9-2) 관계의 종류
// 1> 1:1(One To One) : 관계에 참여하는 각각의 엔터티는 관계 맺는 다른 엔터티에 대해서 다른 하나의 관계만 가짐
// 2> 1:N(One To Many) : 관계 참여하는 각각의 엔터티, 관계 맺는 다른 엔터티에 대해서 하나 이상의 수 관계 가짐.
// 3> M:M(Many To Many) : 관계 참여하는 각각의 엔터티, 관계 맺는 다른 엔터티에 대해서 하나 이상의 수 관계 가짐.
//                      =>반대 방향도 동일하게 관계 참여

// 10. ERD(Entity Relation Diagram)
// - 시스템 엔터티들이 무엇이 있고 어떤 관계 나타내는 다이어그램
