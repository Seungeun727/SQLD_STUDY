// - 엔터티와 속성들을 테이블, 컬럼들로 변환
// - 테이블과 관계 시각화 => 설계 문제점 파악
// - 서로 다른 시스템 요소/서로 다른 관계 식별 도움.

// 10-1) ERD 작성 순서
// 1. 엔터티 작성 => 2. 엔터티 적절하게 배치 => 3. 엔터티 관계 설정
// 4. 관계명 기술함 => 5.  관계의 참여도 기술함 => 6. 관계의 필수 여부를 기술함